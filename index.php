Kenya AIDS Indicator Survey (KAIS 2012) estimated that there were 2.6 million OVC in Kenya in 2012, of whom 1.8 million were orphans and 750,000 were vulnerable. Among orphans, 15% were double orphans. Over one-third of all the OVC were aged between 10 and 14 years. Households with â‰¥1 OVC (12% of all households) were usually in the lowest 2 wealth quintiles, and 22% of OVC households had experienced moderate or severe hunger. Receipt of OVC support services was low for medical (3.7%), psychological (4.1%), social (1.3%), and material support (6.2%); educational support was slightly more common (11.5%). Orphanhood among children aged below 15 years increased from 1993 to 2003 (P < 0.01) but declined from 2003 to 2012 (P < 0.01).<br><br>
The 2.6 million OVC constitute a significant proportion of Kenya's population aged below 18 years. Special attention should be paid to OVC to prevent further vulnerability and ensure their well-being and development as they transition into adulthood.
